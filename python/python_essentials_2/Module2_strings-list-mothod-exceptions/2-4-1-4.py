# 2.4.1.4 - Strings vs. numbers: str() & int() & float() functions

# how to convert a number (an integer or a float) into a string, and vice versa

# str() function:
itg = 13
flt = 1.3
si = str(itg)
sf = str(flt)

print(si + ' ' + sf)

# int() & float() functions:
si = '13'
sf = '1.3'
itg = int(si)
flt = float(sf)

print(itg + flt)

