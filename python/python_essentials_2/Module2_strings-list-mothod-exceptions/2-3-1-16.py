#2.3.1.16 - swapcase() & title() & upper() methods

# swapcase() method:
    # makes a new string by swapping the case of all letters within the source string
        # lower-case characters become upper-case and vice versa

# Demonstrating the swapcase() method:
print("I know that I know nothing.".swapcase())

print("-------------")

# title() method:
    # changes every word's first letter to upper-case, turning all other ones to lower-case

# Demonstrating the title() method:
print("I know that I know nothing. Part 1.".title())

print("-------------")

# upper() method:
    # makes a copy of the source string => replaces all lower-case letters with their upper-case counterparts

# Demonstrating the upper() method:
print("I know that I know nothing. Part 2.".upper())
