#2.2.1.9 - Python strings are immutable

# strings are immutable
# only thing you can do with a string is delete the entire string

#example
alphabet = "abcdefghijklmnopqrstuvwxyz"
print(alphabet)

alphabet = "1234567890"
print(alphabet) 
print("--")

# you can't use append() method, or the insert() method
# you can use del; but only to delete the entire variable not a specific part of the string.

